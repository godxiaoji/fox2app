<script>
import { inArray } from '../../helpers/util'
import { SDKKey } from '../../config'

const TYPE_NAMES = [
  'success',
  'success_no_circle',
  'info',
  'warn',
  'waiting',
  'cancel',
  'download',
  'search',
  'clear',
  // 不对外公开
  'upload',
  'unfold',
  'forward',
  'back',
  'loading',
  'close',
  'radio',
  'radio_checked',
  'checkbox',
  'checkbox_checked'
]

export default {
  name: SDKKey + '-icon',
  props: {
    type: {
      validator(value) {
        return inArray(value, TYPE_NAMES)
      },
      required: true
    },
    size: {
      validator(value) {
        return !isNaN(parseFloat(value))
      },
      default: -1
    },
    color: {
      type: String,
      default: ''
    }
  },
  data() {
    return { prefix: SDKKey }
  },
  computed: {
    iconColor() {
      if (this.color) {
        return {
          fill: this.color
        }
      }
      return null
    },
    iconSize() {
      if (this.size >= 0) {
        return {
          width: this.size + 'px',
          height: this.size + 'px'
        }
      }
      return null
    },
    className() {
      return 'type--' + this.type
    }
  },
  watch: {},
  created() {},
  ready() {},
  mounted() {},
  updated() {},
  attached() {},
  methods: {
    onClick(e) {
      this.$emit(e.type, e)
    }
  }
}
</script>

